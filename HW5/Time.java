/**
 * This class contains a constructor for a Time object, which is simply a way to store a given hour and minute within
 * an object.
 */
public class Time {

    private int hour;
    private int minute;

    /**
     * An object that stores a given hour and minute (both ints) .
     * @param hour
     * @param minute
     */
    public Time(int hour, int minute) {
        this.hour = hour;
        this.minute = minute;
    }


}
